
package citmemoryjj;

public class IndividualProgramming {
    
    public void computeSum() {
        
        int[] numbers = {1,2,3,4,5};
        int sum = 0;
    
        for (int a : numbers) {
            sum += a;
        }
        System.out.println("The sum of 1, 2, 3, 4 and 5 is " + sum + ".");
    }
    
}
